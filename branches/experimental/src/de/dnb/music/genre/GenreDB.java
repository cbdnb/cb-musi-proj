	/**
	 * 
	 * Findet den längsten Match im String s.
	 * Die Suche erfolgt linear.
	 * 
	 * @param parseString nicht null
	 * @return		die Gattung oder null
	 */
	public static Genre matchGenre(final String parseString) {
		if (parseString == null)
			throw new IllegalArgumentException(
					"Null-String an matchGenre()Übergeben");
		if (parseString.length() == 0)
			return null;
		
		Pair<String, String[]> slp = 
				StringUtils.findLongestPrefix(parseString, DATA, SING, PLUR);
		if (slp != null) {
			String[] line = slp.second;
			String match = slp.first;
			Genre g = buildGenre(line);
			g.match = match;
			g.rest = parseString.substring(match.length());
			
			return g;
			
		}		
		
		return null;

	}

	/**
	 * @param line
	 * @return
	 */
	private static Genre buildGenre(String[] line) {
		Genre g = new Genre();

		g.plural = line[PLUR];
		g.singular = line[SING];
		g.nid = line[NID];
		g.idn = line[IDN];
		g.swd = line[SWD];
		return g;
	}
	
	public static Set<Genre> getAllGenres(){
		Set<Genre> genSet = new TreeSet<Genre>();
		for (String[] genreS : DATA) {
			genSet.add(buildGenre(genreS));
		}
		return genSet;
	}
	
	
